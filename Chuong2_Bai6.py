#Trình bày ý nghĩa toán tử /,//,%,**,and,or,is
"""
/
: Phép chia, trả về kết quả là một số thực (kể cả khi kết quả là số nguyên).
Ví dụ: 10 / 2 sẽ trả về 5.0. 
//
: Phép chia lấy nguyên, trả về kết quả là số nguyên và bỏ qua phần thập phân.
Ví dụ: 10 // 3 sẽ trả về 3. 
%
: Phép toán lấy phần dư của phép chia.
Ví dụ: 10 % 3 sẽ trả về 1 (vì 10 chia cho 3 dư 1).
**** : Phép toán lũy thừa, tức là ab có nghĩa là a mũ b.
Ví dụ: 2 ** 3 sẽ trả về 8 (2^3).
Toán tử logic
and
: Toán tử logic AND, trả về True chỉ khi cả hai toán hạng đều là True. Nếu một trong hai là False, kết quả sẽ là False.
Ví dụ: True and True là True; True and False là False. 
or
: Toán tử logic OR, trả về True nếu ít nhất một trong hai toán hạng là True. Chỉ khi cả hai đều là False, kết quả mới là False.
Ví dụ: True or False là True; False or False là False. 
is
: Toán tử so sánh, kiểm tra xem hai đối tượng có phải là cùng một đối tượng trong bộ nhớ hay không. Toán tử này trả về True nếu chúng là cùng một đối tượng, ngược lại trả về False.
Ví dụ: Một biến x tham chiếu đến một danh sách và một biến y tham chiếu đến cùng danh sách đó thì x is y sẽ là True. 

"""